package Logica;

public class Servicio {
	//Atributos//
	private String nombre;
	private int precio;
	private String horario;
	private String tipoCobro;
	private String ubicacion;
	private String dias;
	
	//Constructor//
	public Servicio(String nombre, int precio, String horario, String tipoCobro, String ubicacion, String dias) {
	this.nombre=nombre;
	this.precio=precio;
	this.horario=horario;
	this.tipoCobro=tipoCobro;
	this.ubicacion=ubicacion;
	this.dias=dias;
	}
	

}
